        public void drawBitmap(int left, int top, byte[] bmp, int width, int height, Color fg, Color bg)
        {
            byte[] masks = { 0x80, 0x40, 0x20, 0x10, 0x08, 0x04, 0x02, 0x01 };
            int rowlen = width >> 3;
            if (width % 8 > 0) rowlen++;
            for (int y = 0; y < height; y++)
            {
                int y1 = y + top;
                if (y1 >= m_CurrentBitmap.Height) continue;
                int rowstart = rowlen * y;
                for (int x = 0; x < width; x++)
                {
                    int x1 = x + left;
                    if (x1 >= m_CurrentBitmap.Width) continue;
                    int x8 = x >> 3;
                    byte bmpByte = bmp[rowstart + x8];
                    Color pltColor = ((bmpByte & masks[x % 8]) == 0) ? bg : fg;
                    m_CurrentBitmap.SetPixel(x1, y1, pltColor);
                }
            }
            m_GraphicsUpdatePending = true;
        }

        public void outputDigitString( int x, int y, string src, Color fg, Color bg)
        {
            for( int i=0; i<src.Length; i++){
                outputDigit( x, y, src[i], fg, bg);
                x+=16;
            }
        }

        public void outputDigit( int x, int y, char d, Color fg, Color bg)
        {
            int index = _calcCharacters.IndexOf( d);
            if( index < 0){
                drawBitmap(x, y, _m_SevenSegment_Font, 16, 30, bg, bg);
                return;     
            }
            int shift = 54 * index;
            List<byte> tmp = new List<byte>();
            for (int i = 0; i < 54; i++) tmp.Add(_m_SevenSegment_Font[shift + i]);
            drawBitmap(x, y, _m_SevenSegment_Font, 16, 1, bg, bg);
            drawBitmap(x, y+1, tmp.ToArray(), 16, 27, fg, bg);
            drawBitmap(x, y + 28, _m_SevenSegment_Font, 16, 2, bg, bg);
        }

        public void outputCharString(int x, int y, string src, Color fg, Color bg)
        {
            byte[] eSrc = Encoding.GetEncoding(1251).GetBytes( src);
            int len = eSrc.Length;
            for( int i=0; i<len; i++)
            {
                byte b = eSrc[i];  
                outputChar( x, y, b, fg, bg);
                x += 11;
            }
        }

        public void outputChar( int x, int y, byte c, Color fg, Color bg)
        {
            uint shift = (uint)c << 5;
            List<byte> tmp = new List<byte>();
            for (int i = 0; i < 32; i++) tmp.Add(_m_Nixed_Font[shift+i]);
            drawBitmap(x, y, _m_Nixed_Font, 11, 3, bg, bg);
            drawBitmap(x, y+3, tmp.ToArray(), 11, 16, fg, bg);
            drawBitmap(x, y+19, _m_Nixed_Font, 11, 1, bg, bg);
        }
